import UIKit

extension UIColor {
    static var foilRed: UIColor {
        return UIColor(red: 181 / 255, green: 38 / 255, blue: 26 / 255, alpha: 1)
    }
    
    static var foilGreen: UIColor {
        return UIColor(red: 0/255, green: 174/255, blue: 141/255, alpha: 1)
    }
    
    static var darkBackground: UIColor {
       return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }
}
