import UIKit

extension UIColor {
    static var lightBackground: UIColor {
        return UIColor(red: 220/255, green: 220/255, blue: 220/255, alpha: 1)
    }

    static var darkBackground: UIColor {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }

    static var zeusLight: UIColor {
        return UIColor(red: 123/255, green: 187/255, blue: 184/255, alpha: 1)
    }

    static var zeusDark: UIColor {
        return UIColor(red: 14/255, green: 62/255, blue: 95/255, alpha: 1)
    }
}
